// called by onclick attributes instead
// unique for each article because of localStorage variables
function addToCart() {
    var total=localStorage.getItem('checkout');
    total++;
    localStorage.setItem('checkout',total);
    document.querySelector('#checkout').innerHTML=total;

    var ring0 = localStorage.getItem('id1');
    if(ring0 == null) {
        ring0 = 1;
    } else {
        ring0++;
    }
    localStorage.setItem('id1', ring0);
}

